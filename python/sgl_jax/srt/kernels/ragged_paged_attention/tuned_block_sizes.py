def get_tuned_block_sizes(
    q_dtype,
    kv_dtype,
    actual_num_q_heads,
    actual_num_kv_heads,
    head_dim,
    page_size,
    max_num_tokens,
    pages_per_seq,
    causal=True,
) -> tuple[int, int]:
    if not causal:
        # FIXME(pc) hack this to avoid oom when precompile, currently, we still have no better choice for non-causal's mask
        # this should be optimied future
        return 4, 4
    """Look up for the best (num_kv_pages_per_blk, num_queries_per_blk) from auto-tuned table."""
    tpu_version = get_tpu_version()

    if tpu_version < 4:
        raise NotImplementedError("TPU version must be 4 or higher.")
    keys = get_simplified_key(
        page_size,
        q_dtype,
        kv_dtype,
        actual_num_q_heads,
        actual_num_kv_heads,
        head_dim,
        max_num_tokens,
    )

    device_name = keys[0]

    # Default block sizes.
    bkv_p, bq = (1024 // page_size, 32)
    if tpu_version == 4:
        # TPUv4 has much smaller VMEM size so we pick fixed block sizes.
        bkv_p, bq = (512 // page_size, 32)
    else:
        if device_name in TUNED_BLOCK_SIZES and keys[1:] in TUNED_BLOCK_SIZES[device_name]:
            bkv_p, bq = TUNED_BLOCK_SIZES[device_name][keys[1:]]
        else:
            logger.info(
                "Tuned RPA block sizes not found for %s: page_size=%s, actual_num_q_heads=%s, "
                "actual_num_kv_heads=%s, head_dim=%s, max_num_tokens=%s, pages_per_seq=%s.",
                device_name,
                page_size,
                actual_num_q_heads,
                actual_num_kv_heads,
                head_dim,
                max_num_tokens,
                pages_per_seq,
            )
            logger.info("Using default block size: bkv_p=%s, bq=%s.", bkv_p, bq)

    # if bkv_p != 16 or bq != 16:
    #     raise ValueError(
    #         f"Invalid block sizes: {keys=}, {bkv_p=}, {bq=}. Please check {page_size=}, {actual_num_q_heads=}, {actual_num_kv_heads=}, {head_dim=}, {max_num_tokens=}, {pages_per_seq=}"
    #     )

    return (min(pages_per_seq, bkv_p), min(max_num_tokens, bq))


