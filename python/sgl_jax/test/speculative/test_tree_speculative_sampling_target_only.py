def _build_inputs(tc):
    bs = tc["bs"]
    num_draft = tc["num_draft"]
    num_spec_step = tc["num_spec_step"]

    candidates = jnp.array(tc["candidates"], dtype=jnp.int32)
    retrive_index = jnp.array(tc["retrive_index"], dtype=jnp.int32)
    retrive_next_token = jnp.array(tc["retrive_next_token"], dtype=jnp.int32)
    retrive_next_sibling = jnp.array(tc["retrive_next_sibling"], dtype=jnp.int32)
    uniform_samples = jnp.array(tc["uniform_samples"], dtype=jnp.float32)
    uniform_samples_for_final_sampling = jnp.array(
        tc["uniform_samples_for_final_sampling"], dtype=jnp.float32
    )
    target_probs = jnp.array(tc["target_probs"], dtype=jnp.float32)
    draft_probs = jnp.array(tc["draft_probs"], dtype=jnp.float32)

    predicts = jnp.full((bs * num_draft,), -1, dtype=jnp.int32)
    accept_index = jnp.full((bs, num_spec_step), -1, dtype=jnp.int32)
    accept_token_num = jnp.full((bs,), 0, dtype=jnp.int32)

    return dict(
        predicts=predicts,
        accept_index=accept_index,
        accept_token_num=accept_token_num,
        candidates=candidates,
        retrive_index=retrive_index,
        retrive_next_token=retrive_next_token,
        retrive_next_sibling=retrive_next_sibling,
        uniform_samples=uniform_samples,
        uniform_samples_for_final_sampling=uniform_samples_for_final_sampling,
        target_probs=target_probs,
        draft_probs=draft_probs,
        threshold_single=float(tc["threshold_single"]),
        threshold_acc=float(tc["threshold_acc"]),
    )


